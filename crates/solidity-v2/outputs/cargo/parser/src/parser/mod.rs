/// Ambient state the grammar's actions need.
///
/// These are bundled into a single struct and threaded through the grammar as
/// one `&mut Ctx` parameter, rather than as several separate parameters. LALRPOP
/// forwards grammar parameters through every reduction, so passing a single
/// pointer (and dereferencing its fields only where needed) keeps the hot
/// reduction path cheap.
pub(crate) struct GrammarCtx<'a> {
    /// Source text being parsed (consumed by terminal constructors).
    pub source: &'a str,
    /// Id of the file being parsed, used when reporting diagnostics.
    pub file_id: &'a str,
    /// Diagnostics accumulated during parsing.
    pub diagnostics: DiagnosticCollection,
}

/// The output of a parse operation, containing both the source unit and any diagnostics.
#[derive(Debug, PartialEq)]
pub struct ParseOutput {
    pub source_unit: SourceUnit,
    pub diagnostics: DiagnosticCollection,
}

/// A Parser for Solidity Source Units
///
/// TODO(v2): Error recovery, for now we return an empty [`SourceUnit`] on error
/// TODO(v2): Make the parser skip unique tokens, performance improvement
#[derive(Default)]
pub struct Parser;

impl Parser {
    pub fn parse(file_id: &str, source: &str, language_version: LanguageVersion) -> ParseOutput {
        let lexer = Lexer::new(source, language_version);
        let parser = grammar::SourceUnitParser::new();

        let mut ctx = GrammarCtx {
            source,
            file_id,
            diagnostics: DiagnosticCollection::default(),
        };
        let result = parser.parse(&mut ctx, lexer);
        match result {
            Ok(source_unit) => {
                // TODO(v2): these tests should really go through 'CompilationUnit' once it is ready.
                // This way, we won't have to call individual validation APIs.
                // All errors should be collected during the compilation unit construction.
                validate_syntax_version(
                    &source_unit,
                    language_version,
                    file_id,
                    &mut ctx.diagnostics,
                );
                ParseOutput {
                    source_unit,
                    diagnostics: ctx.diagnostics,
                }
            }
            Err(e) => {
                convert_parse_error(file_id, &mut ctx.diagnostics, e);
                ParseOutput {
                    source_unit: new_source_unit(new_source_unit_members(vec![])),
                    diagnostics: ctx.diagnostics,
                }
            }
        }
    }
}

/// Convert a LALRPOP parse error into a `DiagnosticCollection`.
fn convert_parse_error(
    file_id: &str,
    diagnostics: &mut DiagnosticCollection,
    value: lalrpop_util::ParseError<usize, LexemeKind, ()>,
) {
    /// This function transforms the `String` representation returned by LALRPOP into an instance of `TerminalKind`
    ///
    /// TODO(v2): We may be able to improve on this if there's room for returning a discriminant instead of a string representation.
    /// [Ongoing discussion](https://github.com/lalrpop/lalrpop/issues/1089#issuecomment-4011323139)
    fn convert_expectations(expected: &[String]) -> BTreeSet<TerminalKind> {
        let mut expected: BTreeSet<TerminalKind> = expected
            .iter()
            .map(|str| str.strip_prefix("L_").unwrap())
            .map(|str| str.parse::<LexemeKind>().unwrap())
            .map(|lexeme| TerminalKind::from(&lexeme))
            .collect();

        // This is a heuristic to improve the error message when an identifier is expected.
        //
        // Due to certain keywords being unreserved in Solidity, and therefore context dependent,
        // we assume that if an identifier is expected, then these keywords are only expected as
        // identifiers, and not as keywords.
        if expected.contains(&TerminalKind::Identifier) {
            expected.retain(|terminal| !terminal.is_unreserved_keyword());
        }

        expected
    }

    match value {
        lalrpop_util::ParseError::UnrecognizedEof { location, expected } => {
            diagnostics.push(
                file_id.to_owned(),
                location..location,
                UnexpectedEof {
                    expected: convert_expectations(&expected),
                },
            );
        }
        lalrpop_util::ParseError::UnrecognizedToken {
            token: (left, lexeme, right),
            expected,
        } => {
            diagnostics.push(
                file_id.to_owned(),
                left..right,
                UnexpectedTerminal {
                    found: TerminalKind::from(&lexeme),
                    expected: convert_expectations(&expected),
                },
            );
        }
        lalrpop_util::ParseError::ExtraToken {
            token: (left, lexeme, right),
        } => {
            diagnostics.push(
                file_id.to_owned(),
                left..right,
                ExtraTerminal {
                    found: TerminalKind::from(&lexeme),
                },
            );
        }
        lalrpop_util::ParseError::User { .. } => panic!("The parser should never return a user error, since we're not using any custom error types in our grammar"),
        lalrpop_util::ParseError::InvalidToken { .. } => panic!("The parser should never return an invalid token error, since it's not using the default lexer"),
    }
}

/// Iterate over the lexemes and their offsets
///
/// TODO(v2): This iterator skips all trivia, we'll want to include it in
/// future versions
impl Iterator for Lexer<'_> {
    type Item = Result<(usize, LexemeKind, usize), ()>;

    fn next(&mut self) -> Option<Self::Item> {
        while let Some(lexeme) = self.next_lexeme() {
            if !lexeme.kind.is_trivia() {
                return Some(Ok((lexeme.range.start, lexeme.kind, lexeme.range.end)));
            }
        }
        None
    }
}
